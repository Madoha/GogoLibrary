let alertCallCount = 0;
const tileSize = 15; 

const MapComponent = () => {
  const [characterPosition, setCharacterPosition] = useState({ x: 30, y: 30 });
  const [totalItems, setTotalItems] = useState();
  const [showCongratulations, setShowCongratulations] = useState(false);

  useEffect(() => {
    if (alertCallCount === totalItems) {
      // Воспроизведение звука поздравления
      setShowCongratulations(true);
    }
  }, [alertCallCount, totalItems]);

  useEffect(() => {
    let count = 0;
    mapLayout.forEach(row => {
      row.forEach(item => {
        if (item === 2 || item === 3) count++;
      });
    });
    setTotalItems(count);
  }, []);
 


  useEffect(() => {
    const handleKeyDown = (event) => {
      const { x, y } = characterPosition;
      const step = tileSize;
      let newX = x;
      let newY = y;

      switch (event.key) {
        case 'w':
          newY = Math.max(y - step, 0);
          break;
        case 's':
          newY = Math.min(y + step, (mapLayout.length - 1) * tileSize);
          break;
        case 'a':
          newX = Math.max(x - step, 0);
          break;
        case 'd':
          newX = Math.min(x + step, (mapLayout[0].length - 1) * tileSize);
          break;
        default:
          return;
      }

      const newTileX = Math.floor(newX / tileSize);
      const newTileY = Math.floor(newY / tileSize);
      if (mapLayout[newTileY][newTileX] !== 1) { // 1 - это стенка
        setCharacterPosition({ x: newX, y: newY });
        if (mapLayout[newTileY][newTileX] === 2) { // 2 - это Книгу
          alertCallCount += 1;
          alert('Вы подобрали книгу!');
          mapLayout[newTileY][newTileX] = 0; // Удаляем артефакт
        }
        if (mapLayout[newTileY][newTileX] === 3) { // 3 - это концтовары
          alertCallCount += 1;
          alert('Вы подобрали концтовары!');
          mapLayout[newTileY][newTileX] = 0; // Удаляем еду
        }
      }
      
    };

    window.addEventListener('keydown', handleKeyDown);
    return () => window.removeEventListener('keydown', handleKeyDown);
  }, [characterPosition]);
  return (
    <div className="map-container">
      <p>Collected Items: {alertCallCount}/{totalItems}</p>
      <div className="map">
      {showCongratulations && <div className="congratulations">Поздравляем!</div>}
        {mapLayout.map((row, rowIndex) =>
          row.map((tile, colIndex) => {
            const left = colIndex * tileSize;
            const top = rowIndex * tileSize;
            return (
              <div
                key={`${rowIndex}-${colIndex}`}
                className={`tile ${tile === 1 ? 'wall' : ''} ${tile === 2 ? 'book' : ''} ${tile === 3 ? 'pen' : ''} ${tile === 0 ? 'back' : ''}`}
                style={{ left, top, width: tileSize, height: tileSize}} 
              />
            );
          })
        )}
        <div className="character" style={{ left: characterPosition.x, top: characterPosition.y }}>
        <img src={CharacterImg} alt="Character" />
        </div>
      </div>
      <div>
    </div>
</div>
  );
};

export default MapComponent;
